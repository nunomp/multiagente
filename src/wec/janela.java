    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        int selectedPort = port;

        int reply = JOptionPane.showConfirmDialog(this, "Default Port for communication is " + port + ".Do you confirm?", "Change port?", JOptionPane.YES_NO_OPTION);

        if (reply == JOptionPane.NO_OPTION) {
            String newPort = JOptionPane.showInputDialog(this, "Enter port number", JOptionPane.OK_OPTION);
            selectedPort = Integer.parseInt(newPort);
        }

        initWorker(selectedPort);
        lockStart();

        
        
        

    }//GEN-LAST:event_jButton3ActionPerformed

