    public void initWorker(int selport) {

        //in case of running all the agents in one single computer (container), for instance in a testing situation, every agent's tcp daemon
        // will have a distinct port. This step will ask for port definition
        this.processo = new daemon(this, selport, this.data);
        processo.start();

    }
    
    public void setError(String description){
        
        System.err.println("capacidade "+lista.size());
        if(lista.size()>=maxErrors){
            lista.clear();
        }
        
        lista.addElement(description);
        
    }

